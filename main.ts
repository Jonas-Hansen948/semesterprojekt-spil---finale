function Spawn_loot () {
    Loot2 = sprites.create(assets.image`Boks`, SpriteKind.Loot)
    if (Map_counter == 1) {
        tiles.placeOnTile(Loot2, tiles.getTileLocation(randint(2, 14), randint(2, 14)))
    } else if (Map_counter == 2) {
        tiles.placeOnTile(Loot2, tiles.getTileLocation(randint(2, 18), randint(2, 18)))
    } else if (Map_counter == 3) {
        tiles.placeOnTile(Loot2, tiles.getTileLocation(randint(2, 19), randint(2, 19)))
    } else if (Map_counter == 4) {
        tiles.placeOnTile(Loot2, tiles.getTileLocation(randint(2, 22), randint(2, 22)))
    } else {
        tiles.placeOnTile(Loot2, tiles.getTileLocation(randint(2, 20), randint(2, 20)))
    }
}
