controller.B.onEvent(ControllerButtonEvent.Pressed, function () {
    music.play(music.createSoundEffect(WaveShape.Sine, 1534, 397, 255, 47, 100, SoundExpressionEffect.None, InterpolationCurve.Linear), music.PlaybackMode.InBackground)
    Fremad = false
    if (Minigun != true) {
        if (Timer > 0) {
            if (_2x_skud == true) {
                if (Shotgun == true) {
                    for (let index = 0; index < 2; index++) {
                        Skyd_Tilbage_shotgun_2x()
                    }
                } else {
                    for (let index = 0; index < 2; index++) {
                        Skyd_tilbage_2x()
                    }
                }
            } else if (Shotgun == true) {
                Skyd_Tilbage_shotgun()
            } else if (Fart == true) {
                Skyd_tilbage()
            }
        } else {
            Skyd_tilbage()
        }
    }
})
